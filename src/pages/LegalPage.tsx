export default function LegalPage() {
  const { slug } = useParams<{ slug: string }>();
  const navigate = useNavigate();
  const page = legalContent[slug || ""];

  if (!page) {
    return (
      <div className="fixed inset-0 flex flex-col items-center justify-center bg-background">
        <p className="text-lg font-semibold">পৃষ্ঠা পাওয়া যায়নি</p>
        <button onClick={() => navigate("/")} className="text-primary mt-2">← হোমে ফিরুন</button>
      </div>
    );
  }

  return (
    <div className="fixed inset-0 flex flex-col bg-background z-50">
      <header className="flex items-center gap-3 px-4 h-14 bg-card border-b border-border shrink-0">
        <button onClick={() => navigate(-1)} className="w-9 h-9 rounded-full bg-muted flex items-center justify-center">
          <ArrowLeft size={18} />
        </button>
        <h1 className="text-base font-bold">{page.title}</h1>
      </header>
      <div className="flex-1 overflow-y-auto px-4 py-5">
        <div className="prose prose-sm max-w-none">
          {page.content.split("\n").map((line, i) => {
            if (line.startsWith("**") && line.endsWith("**")) {
              return <h3 key={i} className="text-sm font-bold mt-4 mb-2 text-foreground">{line.replace(/\*\*/g, "")}</h3>;
            }
            if (line.startsWith("•") || line.startsWith("✅") || line.startsWith("❌")) {
              return <p key={i} className="text-[13px] text-muted-foreground ml-2 mb-1">{line}</p>;
            }
            if (line.match(/^\d+\./)) {
              return <p key={i} className="text-[13px] text-muted-foreground ml-2 mb-1">{line}</p>;
            }
            if (line.trim() === "") return <br key={i} />;
            return <p key={i} className="text-[13px] text-foreground mb-2 leading-relaxed">{line.replace(/\*\*/g, "")}</p>;
          })}
        </div>
      </div>
    </div>
  );
}
